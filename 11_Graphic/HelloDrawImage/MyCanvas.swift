import UIKit

class MyCanvas: UIView {
    
    override func drawRect(rect: CGRect) {
        let image = UIImage(named: "Beautiful")            //讀入圖片
        image?.drawAtPoint(CGPointMake(0, 0))              //第一種方法
        image?.drawInRect(CGRectMake(0, 350, 300, 300))    //第二種方法
    }    
}
